    private void postaviModForme(ModeForm mode) {
        this.mode = mode;

        switch (mode) {
            case FORM_KREIRAJ:
                postaviZaKreiranje();
                break;
            case FORM_STORNIRAJ:
                postaviZaStorniranje();
                break;
            case FORM_IZMENI:
                postaviZaIzmenu();
                break;
            case FORM_PRETRAZI:
                postaviZaPregled();
                break;
            default:
                break;
        }
    }

